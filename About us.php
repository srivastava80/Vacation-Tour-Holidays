    <CENTER><FONT COLOR="Black" SIZE="10"><U>About Vacation-Travel & Holidays</U></FONT></CENTER>
    <BR>
    Nurtured from the seed of a single idea ‘to empower the traveller with easy & instant travel bookings providing comprehensive choices’, Vacation-Travel & Holidays is a pioneer in India’s online travel space. Founded by Raj Srivastava in the year 2024, Vacation-Travel & Holidays began its journey by serving the US-India travel market with best-value products & services, powered by robust technology and round-the-clock customer support.

After successfully consolidating its position as a customer-first brand, known for its reliability and transparency, Vacation-Travel & Holidays launched its India operations in 2024.

As low-cost flight carriers were introduced in India, the number of Indians opting for online travel solutions also increased rapidly. And Vacation-Travel & Holidays answered the call of the hour, by bringing the convenience of booking flights, hotels, and holiday packages in just a few clicks.

Over the years, we have partnered with many leading brands from the aviation & hospitality industries, creating fruitful partner relations for business expansion opportunities. We also entered the homestays & villas and continue to procure increased market share market in the same. With this, we also entered the ground transport space and commenced offering cab, bus & train booking services.

What makes our story even stronger is the performance of our newly launched segments, like myBiz—our comprehensive business travel suite and myPartner—an exclusive platform for travel agents. Entering the Gulf market is our latest feat, where we offer power-packed deals on flights & hotels.
   
